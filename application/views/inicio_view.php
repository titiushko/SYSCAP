					<h3>Objetivos</h3>
					<p><b>Objetivo General.</b></p>
					<p>Elaborar el Análisis de requerimientos y el Diseño de la solución del Sistema Informático para apoyar el Control y Administración de Capacitaciones Impartidas por el Área de Formación Virtual de la Gerencia de Tecnologías Educativas del Ministerio de Educación (SYSCAP).</p>
					<p><b>Objetivos Especificos.</b></p>
					<p>
						<ul>
							<li>Determinar los requerimientos del Área de Formación Virtual de la Gerencia de Tecnologías Educativas para ayudar al control y la administración de las capacitaciones y la elaboración de reportes y estadísticas.</li>
							<li>Analizar los requerimientos del Área de Formación Virtual de la Gerencia de Tecnologías Educativas para elaborar el catálogo de requerimientos operativos y desarrollo de SYSCAP.</li>
							<li>Describir los requerimientos del Área de Formación Virtual de la Gerencia de Tecnologías Educativas utilizando casos de uso del modelado orientado a objetos.</li>
							<li>Diseñar el modelo de la base de datos y el diagrama de clases de la solución para optimizar el control y administración de las capacitaciones del Área de Formación Virtual de la Gerencia de Tecnologías Educativas.</li>
							<li>Elaborar el diseño de interfaces de SYSCAP para mensajes de dialogo, pantallas de entrada y pantallas salida.</li>
						</ul>
					</p>
