					<h3>Introducción</h3>
					<p>El presente documento da inicio al desarrollo del proyecto Sistema Informático para apoyar el Control y Administración de Capacitaciones Impartidas por el Área de Formación Virtual de la Gerencia de Tecnologías Educativas del Ministerio de Educación (SYSCAP), con las etapas de análisis y diseño del ciclo de vida de desarrollo de software, permitiendo definir las bases para construir el sistema como producto final.</p>
					<p>La sección Análisis del sistema describe las necesidades del Área de Formación Virtual para SYSCAP utilizando los siguientes elementos:</p>
					<p>
						<ul>
							<li>Determinación de requerimientos.</li>
							<li>
								Listado de requerimientos desglosados en:
								<ul>
									<li>Funcionales.</li>
									<li>De rendimiento.</li>
									<li>De seguridad.</li>
									<li>De desarrollo.</li>
									<li>De implementación.</li>
								</ul>
							</li>
							<li>
								Análisis de requerimientos:
								<ul>
									<li>Diagramas de casos de usos.</li>
									<li> Descripción de casos de usos.</li>
								</ul>
							</li>
							<li>Diagramas de secuencia.</li>
							<li>Diagramas de flujo de trabajo.</li>
						</ul>
					</p>
					<p>La sección Diseño de la solución define los elementos que establecen cómo SYSCAP cumplirá las necesidades del Área de Formación Virtual definidos en el Análisis del sistema y contiene:</p>
					<p>
						<ul>
							<li>Diccionario de datos.</li>
							<li>Diseño de base de datos.</li>
							<li>Diseño de diagrama de clases.</li>
							<li>Diseño de interfaces.</li>
						</ul>
					</p>
